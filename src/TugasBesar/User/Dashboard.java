    void tampil() {
        try {
            // Step 4 Membuat Query
            String sql = "SELECT * FROM menu";
            model.setRowCount(0);  // Clear existing rows
            model.fireTableDataChanged();

            rs = perintah.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("nama_menu"),
                    rs.getString("harga")
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Database tidak berhasil terkoneksi",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void updateTotal() {
        DefaultTableModel modelPesan = (DefaultTableModel) TablePesan.getModel();

        double total = 0;
        for (int i = 0; i < modelPesan.getRowCount(); i++) {
            Object subtotalObject = modelPesan.getValueAt(i, 3); // Assuming subtotal is in the fourth column
            if (subtotalObject != null) {
                total += (double) subtotalObject;
            }
        }

        Total.setText(String.valueOf(total));
    }

