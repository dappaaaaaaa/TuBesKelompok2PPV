    private void HapusPesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusPesananActionPerformed
        // TODO add your handling code here:
        int selectedRow = TablePesan.getSelectedRow();
        if (selectedRow >= 0) {
            DefaultTableModel modelPesan = (DefaultTableModel) TablePesan.getModel();
            modelPesan.removeRow(selectedRow);

            // Update total setelah menghapus pesanan
            updateTotal();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih pesanan yang akan dihapus", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_HapusPesananActionPerformed

