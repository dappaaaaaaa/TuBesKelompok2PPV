    private void pesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanActionPerformed
        // TODO add your handling code here:
        String namaMenu = NamaTf.getText();
        String harga = HargaTf.getText();
        int jumlahPesan = (int) JumlahPesan.getValue();

        // Validasi agar jumlah pesan tidak kurang dari 1
        if (jumlahPesan < 1) {
            JOptionPane.showMessageDialog(this, "Jumlah pesanan harus lebih dari 0", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; // Keluar dari method jika jumlah pesan kurang dari 1
        }

        // Calculate subtotal
        double subtotal = Double.parseDouble(harga) * jumlahPesan;

        // Add the selected item to the TablePesan table
        DefaultTableModel modelPesan = (DefaultTableModel) TablePesan.getModel();

        // Insert the new row at the beginning of the table
        modelPesan.insertRow(0, new Object[]{namaMenu, harga, jumlahPesan, subtotal});

        // Update the total field
        updateTotal();
    }//GEN-LAST:event_pesanActionPerformed

