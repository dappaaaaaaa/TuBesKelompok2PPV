    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int selectedRow = table.getSelectedRow();
        if (selectedRow >= 0) {
            try {
                // Assuming that the 'foto' column is the fourth column in the table
                String nama = model.getValueAt(selectedRow, 0).toString(); // Assuming 'nama_menu' is in the first column
                NamaTf.setText(nama);

                if (model.getColumnCount() > 1) {
                    String harga = model.getValueAt(selectedRow, 1).toString(); // Assuming 'harga' is in the second column
                    HargaTf.setText(harga);
                } else {
                    // Handle the case where the model doesn't have a second column
                    // You may display a message, log an error, or take appropriate action
                    HargaTf.setText("N/A"); // Set a default value or handle it as needed
                }

                // Fetch the Blob directly from the database based on the 'id_Menu'
                String sql = "SELECT foto FROM menu WHERE nama_menu = ?";
                try (PreparedStatement pstmt = con.prepareStatement(sql)) {
                    pstmt.setString(1, nama);
                    try (ResultSet resultSet = pstmt.executeQuery()) {
                        if (resultSet.next()) {
                            Blob image = resultSet.getBlob("foto");

                            if (image != null) {
                                byte[] bytea = image.getBytes(1, (int) image.length());
                                try (FileOutputStream fos = new FileOutputStream("C:\\Users\\ACER\\OneDrive\\Documents\\Kuliah\\Semester 3\\Pemograman Visual\\PraktikumVisual3B\\src\\ImagesTugasBesar\\Menu\\download.jpg")) {
                                    fos.write(bytea);
                                } catch (IOException e) {
                                    e.printStackTrace(); // Handle IO exception
                                }

                                ImageIcon icon = new ImageIcon(bytea);
                                inputfoto.setIcon(icon);
                            }
                        }
                    }
                    JumlahPesan.setEnabled(true);
                    JumlahPesan.setValue(1);
                } catch (SQLException e) {
                    e.printStackTrace(); // Handle SQLException
                }
            } catch (Exception ex) {
                ex.printStackTrace(); // Handle other exceptions
            }
        }
    }//GEN-LAST:event_tableMouseClicked

